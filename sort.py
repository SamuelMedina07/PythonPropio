# sort() ordenando desde la a-z
cars=['bmw','audi','toyota','subaru']
cars.sort()
print(cars)
#sort(reverse=true) ordenando desde la z-a
cars.sort(reverse=True)
print(cars)
#sorted() lo ordena temporalmente 
print(sorted(cars))
print(sorted(cars,reverse=True))
#reverse() ordena del ultimo elemento hasta el primero de la lista
autos=['bmw','audi','toyota','subaru']
autos.reverse()
print(autos)
#len() encuentra la longitud de elementos
print(len(autos))



